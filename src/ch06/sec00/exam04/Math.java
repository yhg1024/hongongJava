package ch06.sec00.exam04;

public class Math {
	// 메소드만 가지고 있는 유틸리티 클래스도 많이 사용합니다.
	// 덧셈 메소드(정수, 정수)
	int add(int x, int y) {
		return x + y;
	}

	// 뺄셈메소드 ( 정수, 정수)
	int sub(int x, int y) {
		return x - y;
	}

	// 뺄셈메소드 ( 정수, 정수)
	int mul(int x, int y) {
		return x * y;
	}

	// 뺄셈메소드 ( 정수, 정수)
	int div(int x, int y) {
		return x / y;
	}

	// 뺄셈메소드 ( 정수, 정수)
	int mod(int x, int y) {
		return x % y;
	}
}
